# Python respeta la jerarquía de las operaciones matematicas

"""
   Flotantes

   Python llama Floats a cualquier número que tenga un punto decimal.
"""
print("Flotante: 1.5")
print(1.5)

print("Suma de un entero y un flotante: 1 + 1.5")
print(1 + 1.5)

print("Multiplicación de un entero y un flotante: 2 * 1.5")
print(2 * 1.5)


